//Adicionando e removendo elementos de um Array

let pokemons = ['Pikachu']
console.log(pokemons)

//Adicionando elemtos no final do Array
pokemons.push('Charmander')
console.log(pokemons)

//Removendo o ultimo elemento de um item do Array
pokemons.pop()
console.log(pokemons)

//Remove o primeiro elemento do Array
pokemons.shift()
console.log(pokemons)